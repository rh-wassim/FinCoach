const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const [lang, setLangState] = useState(() => localStorage.getItem('lang') || 'fr');

  function setLang(l) {
    setLangState(l);
    localStorage.setItem('lang', l);
  }

  function t(key, vars = {}) {
    const keys = key.split('.');
    let val = T[lang];
    for (const k of keys) {
      val = val?.[k];
      if (val === undefined) return key;
    }
    if (typeof val === 'string' && Object.keys(vars).length) {
      return val.replace(/\{(\w+)\}/g, (_, k) => (vars[k] !== undefined ? vars[k] : `{${k}}`));
    }
    return val;
  }

  function tCat(name) {
    return T[lang]?.cat?.[name] || name;
  }

  return (
    <I18nContext.Provider value={{ lang, setLang, t, tCat }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
